	void CGameMap::LoadBitmap()
	{
		wall.LoadBitmap(IDB_WALL_BLUE);
		Food.LoadBitmap(IDB_FOOD);
	}

	void CGameMap::OnShow()
	{
		for (int i = 0; i < 31; i++)
		{
			for (int j = 0; j < 28; j++)
			{
				switch (map[i][j])
				{
				case 0:
					Food.SetTopLeft(X + (MW*j), Y + (MH*i));
					Food.ShowBitmap();
					break;
				case 1:
					wall.SetTopLeft(X + (MW*j), Y + (MH*i));
					wall.ShowBitmap();
					break;
				default:
					break;
				}
			}
		}
	}

