	void CGameMap::OnShow()
	{
		int currentFoodIndex = 0;

		for (int i = 0; i < 31; i++)
		{
			for (int j = 0; j < 28; j++)
			{
				switch (map[i][j])
				{
				case 0:
					//Food.SetTopLeft(X + (MW*j), Y + (MH*i));
					//Food.ShowBitmap();
					allFoods.at(currentFoodIndex).SetTopLeft(X + (MW*j), Y + (MH*i));
					allFoods.at(currentFoodIndex).OnShow();
					currentFoodIndex++;
					break;
				case 1:
					wall.SetTopLeft(X + (MW*j), Y + (MH*i));
					wall.ShowBitmap();
					break;
				default:
					break;
				}
			}
		}
	}
